package com.jokecamera.app

import android.content.Context
import android.content.SharedPreferences

/**
 * Data class representing a joke with setup and punchline
 */
data class Joke(
    val id: Int,
    val setup: String,
    val punchline: String
)

/**
 * Manages the collection of 1000 jokes, tracks which have been told,
 * and ensures no repeats until all jokes have been used.
 */
class JokeManager(context: Context) {
    
    private val prefs: SharedPreferences = context.getSharedPreferences(PREFS_NAME, Context.MODE_PRIVATE)
    private val jokes: List<Joke> = generateJokes()
    private val toldJokeIds: MutableSet<Int> = loadToldJokes()
    
    companion object {
        private const val PREFS_NAME = "joke_manager_prefs"
        private const val KEY_TOLD_JOKES = "told_jokes"
    }
    
    /**
     * Gets the next joke that hasn't been told yet.
     * If all jokes have been told, resets and starts over.
     */
    fun getNextJoke(): Joke {
        // If all jokes have been told, reset
        if (toldJokeIds.size >= jokes.size) {
            resetToldJokes()
        }
        
        // Find a joke that hasn't been told
        val availableJokes = jokes.filter { it.id !in toldJokeIds }
        val joke = availableJokes.random()
        
        // Mark as told
        toldJokeIds.add(joke.id)
        saveToldJokes()
        
        return joke
    }
    
    /**
     * Gets a specific joke by ID (for testing/preview)
     */
    fun getJokeById(id: Int): Joke? = jokes.find { it.id == id }
    
    /**
     * Gets the count of remaining untold jokes
     */
    fun getRemainingCount(): Int = jokes.size - toldJokeIds.size
    
    /**
     * Gets the total joke count
     */
    fun getTotalCount(): Int = jokes.size
    
    /**
     * Resets all jokes to untold status
     */
    fun resetToldJokes() {
        toldJokeIds.clear()
        saveToldJokes()
    }
    
    private fun loadToldJokes(): MutableSet<Int> {
        val stored = prefs.getStringSet(KEY_TOLD_JOKES, emptySet()) ?: emptySet()
        return stored.mapNotNull { it.toIntOrNull() }.toMutableSet()
    }
    
    private fun saveToldJokes() {
        prefs.edit()
            .putStringSet(KEY_TOLD_JOKES, toldJokeIds.map { it.toString() }.toSet())
            .apply()
    }
    
